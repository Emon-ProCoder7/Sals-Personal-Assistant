from http.server import BaseHTTPRequestHandler
import json
import os
import requests

TELEGRAM_TOKEN = os.environ.get("TELEGRAM_BOT_TOKEN")

def send_message(chat_id, text):
    url = f"https://api.telegram.org/bot{TELEGRAM_TOKEN}/sendMessage"
    payload = {
        "chat_id": chat_id,
        "text": text
    }
    response = requests.post(url, json=payload)
    return response.json()

class handler(BaseHTTPRequestHandler):
    def do_GET(self):
        self.send_response(200)
        self.send_header('Content-type', 'text/plain')
        self.end_headers()
        self.wfile.write("Jenny is online! This is the webhook endpoint for the Telegram bot.".encode('utf-8'))

    def do_POST(self):
        try:
            content_length = int(self.headers['Content-Length'])
            post_data = self.rfile.read(content_length)

            print(f"Received webhook data: {post_data.decode('utf-8')}")

            update = json.loads(post_data.decode('utf-8'))

            message = update.get('message', {})
            chat_id = message.get('chat', {}).get('id')
            text = message.get('text')

            if chat_id and text:
                if text.startswith('/start'):
                    response = "Hello! I am Jenny, Sal's Personal Assistant. How can I help you today?"
                else:
                    response = f"You said: {text}"
                
                send_message(chat_id, response)
                print(f"Sent response to chat_id {chat_id}")

            self.send_response(200)
            self.send_header('Content-type', 'application/json')
            self.end_headers()
            self.wfile.write(json.dumps({'status': 'ok'}).encode('utf-8'))

        except Exception as e:
            print(f"Error in webhook handler: {e}")
            self.send_response(500)
            self.send_header('Content-type', 'application/json')
            self.end_headers()
            self.wfile.write(json.dumps({'status': 'error', 'message': str(e)}).encode('utf-8'))
